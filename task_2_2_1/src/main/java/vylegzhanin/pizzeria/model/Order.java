package vylegzhanin.pizzeria.model;

/**
 * Неизменяемая модель заказа пиццерии.
 *
 * <p>Хранит три атрибута: уникальный идентификатор, время поступления заказа
 * и его размер (масса в условных единицах). Используется как передаваемый
 * объект между очередью заказов {@code OrderQueue}, хранилищем {@code Storage}
 * и работниками ({@code Baker}, {@code Courier}).</p>
 *
 * @param id              уникальный идентификатор заказа
 * @param reservationTime метка времени (мс с эпохи Unix) поступления заказа в систему
 * @param size            размер (масса) заказа в условных единицах; влияет на вместимость
 *                        багажника курьера
 */
public record Order(long id, Long reservationTime, int size) {

    /**
     * Вспомогательный конструктор: автоматически проставляет время поступления заказа.
     *
     * @param id   уникальный идентификатор заказа
     * @param size размер (масса) заказа в условных единицах
     */
    public Order(Long id, int size) {
        this(id, System.currentTimeMillis(), size);
    }
}
